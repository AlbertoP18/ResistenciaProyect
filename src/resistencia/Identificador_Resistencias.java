    private void identificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_identificarActionPerformed
        // TODO add your handling code here:
        
        
         String first_color = (String) box1.getSelectedItem();
         String second_color =  (String) box2.getSelectedItem();
         String third_color =  (String) box3.getSelectedItem();
         String fourth_color = (String) box4.getSelectedItem();
         
         int first_value = calc_valor(first_color);
         int second_value = calc_valor(second_color);
         
         int mult = calc_valor(third_color);
         
         int third_value = calc_ceros(mult);
         
         
         String fourth_value = porcentaje(fourth_color);
         
         int value  = first_value * 10 + second_value;
         
         if (third_value != 0){
            String output_final = String.valueOf(value * third_value) + " ohmnios";
            salida.setText(output_final);
            
            salida_p.setText(fourth_value);
         } else {
            String output_final = String.valueOf(value) + " ohmnios";
            salida.setText(output_final);
            salida_p.setText(fourth_value);
            
         }
         
    }//GEN-LAST:event_identificarActionPerformed

