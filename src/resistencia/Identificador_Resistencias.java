    public String porcentaje (String color){
        
        if(color.equals("Cafe")){
            return "1%";
        }
        if(color.equals("Rojo")){
            return "2%";
        }
        if(color.equals("Verde")){
            return "0.5%";
        }
        if(color.equals("Dorado")){
            return "5%";
        }
        if(color.equals("Plateado")){
            return "10%";
        }
        else{
            return "0";
        }
    }
    public int calc_ceros(int cant){
        int numero = 10;
        int num = 10;
        if(cant == 0){
            return 0;
        }
        for(int i = 0 ; i < cant-1; i++){
              numero = numero * num;
            
        }
        return numero;
    }
    public int calc_valor(String color){
        switch (color) {
            case "Negro":
                return 0;
            case "Cafe":
                return 1;
            case "Rojo":
                return 2;
            case "Naranja":
                return 3;
            case "Amarillo":
                return 4;
            case "Verde":
                return 5;
            case "Azul":
                return 6;
            case "Morado":
                return 7;
            case "Gris":
                return 8;
            default:
                return 9;
        }
    }
